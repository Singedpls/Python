#Operadores Relacionales

#Igualdad
resultado = 10 == 20
print(resultado)

#Mayor qué
resultado = 10 > 20
print(resultado)

#Menor qué
resultado = 10 < 20
print(resultado)

#Diferente de
resultado = 10 != 20
print(resultado)

#Menor o igual qué
resultado = 10 <= 20
print(resultado)

#Mayor o igual qué
resultado = 10 >= 20
print(resultado)

#Combinación Operadores Relacionales con Aritméticos
a = 10
b = 20
c = 30
resultado = a+b == c
#resultado = a+b != c
#resultado = a+b >= c
print(resultado)



